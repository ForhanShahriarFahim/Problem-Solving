void solve()
{
    bool first = 0;
    bool second = 0;
    int result = 0;
    int n, m;
    cin >> n >> m;
    vector<string> arr(n);
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }

    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            if (arr[i][j] == '0')
            {
                first = 1;
                for (int k = 0; k < 8; k++)
                {
                    int nx = i + dx[k];
                    int ny = j + dy[k];

                    if (nx < n && nx >= 0 && ny >= 0 && ny < m && arr[nx][ny] == '0')
                    {
                        second = 1;
                    }
                }
            }
            else
            {
                result++;
            }
        }
    }

    if (second)
    {
        cout << result << endl;
    }
    else if (first)
    {
        cout << result - 1 << endl;
    }
    else
    {
        cout << result - 2 << endl;
    }
    }

